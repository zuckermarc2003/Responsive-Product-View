export const getFinalPrice = (price: number, promo: number): number => {
  return promo > 0 ? price * (1 - promo / 100) : price;
};

export const getProductsByType = (type: string): Product[] => {
  if (type === 'all') return PRODUCTS;
  return PRODUCTS.filter(p => p.product_type === type);
};

export const getNewArrivals = (): Product[] => PRODUCTS.filter(p => p.isNew);
